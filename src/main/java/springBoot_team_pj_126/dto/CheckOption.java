package springBoot_team_pj_126.dto;

public class CheckOption {
    private String startWork;
    private String meeting;
    private String goOut;
    private String fieldWork;
    private String endWork;
    private String education;
    private String businessTrip;
    private String rest;
    private String returnWork;

    public CheckOption() {
    }

    public String getStartWork() {
        return startWork;
    }

    public void setStartWork(String startWork) {
        this.startWork = startWork;
    }

    public String getMeeting() {
        return meeting;
    }

    public void setMeeting(String meeting) {
        this.meeting = meeting;
    }

    public String getGoOut() {
        return goOut;
    }

    public void setGoOut(String goOut) {
        this.goOut = goOut;
    }

    public String getFieldWork() {
        return fieldWork;
    }

    public void setFieldWork(String fieldWork) {
        this.fieldWork = fieldWork;
    }

    public String getEndWork() {
        return endWork;
    }

    public void setEndWork(String endWork) {
        this.endWork = endWork;
    }

    public String getEducation() {
        return education;
    }

    public void setEducation(String education) {
        this.education = education;
    }

    public String getBusinessTrip() {
        return businessTrip;
    }

    public void setBusinessTrip(String businessTrip) {
        this.businessTrip = businessTrip;
    }

    public String getRest() {
        return rest;
    }

    public void setRest(String rest) {
        this.rest = rest;
    }

    public String getReturnWork() {
        return returnWork;
    }

    public void setReturnWork(String returnWork) {
        this.returnWork = returnWork;
    }
}
